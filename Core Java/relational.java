public class relational {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a<=b);
        System.out.println(a<b);

        int z=10;
        int y=10;
        System.out.println(z==y);
        System.out.println(z<=y);

        int x1=20;
        System.out.println(x1<=z);
    }
}
